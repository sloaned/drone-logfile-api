

CREATE TABLE IF NOT EXISTS `flight_log_item` (
  `logfile_id` INTEGER NOT NULL,
  `timestamp` VARCHAR(40),
  `aircraft_lon` DECIMAL(30, 16),
  `aircraft_lat` DECIMAL(30, 16),
  `aircraft_altitude` DECIMAL(30, 16),
  `aircraft_speed` DECIMAL(30, 16),
  `aircraft_heading` DECIMAL(30, 16),
  `aircraft_vel_x` DECIMAL(30, 16),
  `aircraft_vel_y` DECIMAL(30, 16),
  `aircraft_vel_z` DECIMAL(30, 16),
  `aircraft_pitch` DECIMAL(30, 16),
  `aircraft_roll` DECIMAL(30, 16),
  `aircraft_yaw` DECIMAL(30, 16),
  `aircraft_satellites` INTEGER,
  `aircraft_motors_on` BOOL,
  `aircraft_flying` BOOL,
  `aircraft_flight_mode` VARCHAR(30),
  `aircraft_flight_mode_value` INTEGER,
  `aircraft_imu_preheating` BOOL,
  `aircraft_ultrasonic_on` BOOL,
  `aircraft_ultrasonic_altitude` DECIMAL(30, 16),
  `aircraft_vision_on` BOOL,
  `aircraft_gps_signal` BOOL,
  `aircraft_gps_signal_value` DECIMAL(30, 16),
  `aircraft_smart_gohome_flight_time_remaining` INTEGER,
  `aircraft_smart_gohome_requesting` BOOL,
  `aircraft_last_updated` VARCHAR(40),
  `battery_power` INTEGER,
  `battery_current` INTEGER,
  `battery_voltage` INTEGER,
  `battery_temperature` INTEGER,
  `battery_charge` INTEGER,
  `battery_last_updated` VARCHAR(40),
  `battery_cell_one` INTEGER,
  `battery_cell_two` INTEGER,
  `battery_cell_three` INTEGER,
  `battery_cell_four` INTEGER,
  `battery_cell_five` INTEGER,
  `battery_cell_six` INTEGER,
  `battery_cell_last_updated` VARCHAR(40),
  `gcs_lat` DECIMAL(30, 16),
  `gcs_lon` DECIMAL(30, 16),
  `gcs_location_last_updated` VARCHAR(40),
  `gimbal_pitch` DECIMAL(30, 16),
  `gimbal_roll` DECIMAL(30, 16),
  `gimbal_yaw` DECIMAL(30, 16),
  `gimbal_mode` DECIMAL(30, 16),
  FOREIGN KEY (`logfile_id`) REFERENCES `flight_logfile` (`logfile_id`)
  ON DELETE CASCADE
  ON UPDATE CASCADE
);